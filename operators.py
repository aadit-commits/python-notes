# Arithematic operators -> ( +, -, *, /, //, % )

print(a+b) # Prints the sum 
print(a-b) # Prints the difference
print(a*b) # Prints the product
print(a/b) # Divides a & b
print(a//b) # Floor Division : Divides and returns the value in int datatype
print(a%b) # Modulo Operator : Prints the remainder

# Logical operators -> ( and, or , not)
# or = at least one condition must be true
# and = both conditions must be true
